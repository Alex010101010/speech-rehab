def load_decisions():
    if not os.path.exists(DECISIONS):
        return {}
    return json.load(open(DECISIONS, encoding="utf-8"))


def main():
    decisions = load_decisions()
    items, dropped, overridden = [], [], 0
    for i, (syl, emoji) in enumerate(WORDS, 1):
        pid = f"sy_{i:03d}"
        dec = decisions.get(pid, {})
        if dec.get("approved") is False:  # отклонено логопедом
            dropped.append(pid)
            continue
        comment = (dec.get("comment") or "").strip()
        if comment:  # логопед прислал исправленное деление
            fixed = [s for s in re.split(r"[/\s]+", comment) if s]
            if "".join(fixed) == "".join(syl):  # то же слово, иное деление
                syl = fixed
                overridden += 1
            else:
                raise SystemExit(
                    f"{pid}: деление в комментарии «{comment}» не складывается "
                    f"в слово «{''.join(syl)}»")
        items.append({
            "id": pid,
            "level": 0 if len(syl) <= 2 else 1,
            "answer": "".join(syl),
            "syllables": syl,
            "emoji": emoji,
        })

    # черновик, пока есть слово без явного одобрения логопеда
    def decided(pid):
        d = decisions.get(pid, {})
        return d.get("approved") is True or bool((d.get("comment") or "").strip())
    is_draft = any(not decided(it["id"]) for it in items)

    doc = {
        "type": "syllables",
        "title": "Соберите слово из слогов",
        "section": "0. Узнавание",
        "draft": is_draft,
        "count": len(items),
        "items": items,
    }
    open(OUT, "w", encoding="utf-8").write(
        json.dumps(doc, ensure_ascii=False, indent=2) + "\n")
    two = sum(1 for it in items if len(it["syllables"]) <= 2)
    print(f"syllables: {len(items)} слов "
          f"({two} двусложных L0, {len(items) - two} трёхсложных L1; "
          f"draft={is_draft}, убрано {len(dropped)}, правок деления {overridden})")
    print(f"  → {OUT} (ассет соберёт tools/build_assets.py)")


if __name__ == "__main__":
    main()
