def load_decisions():
    if not os.path.exists(DECISIONS):
        return {}
    return json.load(open(DECISIONS, encoding="utf-8"))


def main():
    decisions = load_decisions()
    items, dropped = [], []
    # id стабильны по позиции в ITEMS (не сдвигаются при отклонении отдельных)
    for i, (level, theme, prompt, answer, distractors) in enumerate(ITEMS, 1):
        pid = f"ech_{i:03d}"
        if decisions.get(pid, {}).get("approved") is False:  # отклонено логопедом
            dropped.append(pid)
            continue
        items.append({
            "id": pid,
            "level": level,
            "theme": theme,
            "prompt": prompt,
            "options": [answer] + distractors,  # answer первым; виджет перемешает
            "answer": answer,
        })

    # черновик, пока есть задание без явного решения логопеда
    def decided(pid):
        d = decisions.get(pid, {})
        return d.get("approved") is True or bool((d.get("comment") or "").strip())
    is_draft = any(not decided(it["id"]) for it in items)

    data = {
        "type": "endings_choice",
        "title": "Выберите правильную форму слова",
        "section": "V. Грамматика",
        "draft": is_draft,
        "count": len(items),
        "items": items,
    }
    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    levels = {}
    for it in items:
        levels[it["level"]] = levels.get(it["level"], 0) + 1
    print(f"endings_choice: {len(items)} заданий "
          f"(draft={is_draft}, убрано {len(dropped)})")
    print(f"   уровни: {dict(sorted(levels.items()))}")


if __name__ == "__main__":
    main()
