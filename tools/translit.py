#!/usr/bin/env python3
"""Транслитерация ru→slug для имён файлов картинок. Единый источник, чтобы
fetch_arasaac.py (скачивает) и build_picture_word.py (привязывает) давали
ОДИНАКОВЫЕ имена файлов — иначе картинка не найдётся."""

_TR = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'e',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
    'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya', ' ': '_',
}


def slugify(word):
    return ''.join(_TR.get(ch, '') for ch in word.lower()).strip('_')
